    private void AddListaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_AddListaMouseClicked
        DefaultListModel lm;
        int size =0;
        try
        {
            lm =  (DefaultListModel) listaVisual.getModel();
            size = listaVisual.getModel().getSize();
        }catch(Exception e)
        {
            lm = new DefaultListModel();
        }
        
        lm.add(size,lista.getText() + " - " + qnt.getText());
        for(int i = 0;i<Integer.valueOf(qnt.getText());i++)
        {
            nomeListas.add(lista.getText() +" - "+i);
        }
        quantidades.add(qnt.getText());
        listaVisual.setModel(lm);
    }//GEN-LAST:event_AddListaMouseClicked

    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
        int tam = 0;
        for(int i = 0;i<quantidades.size();i++)
        {
            tam += Integer.valueOf(quantidades.get(i).toString().trim());
        }
        System.out.println("TAM:"+tam);
        System.out.println("Nome SIZE:"+nomePessoas.size());
        int total = (tam/nomePessoas.size());
        System.out.print(total);
        DefaultListModel lm = new DefaultListModel();
        int j = 0;
        Collections.shuffle(nomeListas);
        int u=0;
        nomePessoas.add("Sobra");
        for(int i =0;i<nomePessoas.size();i++)
        {
            lm.add(u,"<------ "+nomePessoas.get(i)+" ------>");
            u++;  
            for(j=j;j<total;j++)
            {
                try
                {
                lm.add(u,nomeListas.get(j));
                u++;
                }catch(Exception e)
                {
                }
            }
            j = total;
            total+=total;
            
        }
        Resultado.setModel(lm);
    }//GEN-LAST:event_jButton2MouseClicked

